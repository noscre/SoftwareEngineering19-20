

public class Teilaufgabe{
	private int nr;
	private double time;

	private String ref;
	
	public Teilaufgabe(int n, double t, String ref) {

		nr = n;
		time = t;

		this.ref = ref;
	}
	
	
	
	
	

	
	public int getNr() {
		return nr;
	}
	
	public double getTime() {
		return time;
		
	}
	
	public String getRef() {
		return ref;
		
	}
	

	
	
}
